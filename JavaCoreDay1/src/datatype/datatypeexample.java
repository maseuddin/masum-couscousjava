package datatype;

//import java.lang.*

public class datatypeexample {
	public static void main(String[] args) {
		//all data types
		//declaration datatype name variable name= value
		String name="Masum";
		byte a=2;
		short b=20;
		int c=23456;
		long d=3345678L;
		
		float x=12.55f;
		double y= 645.66;
		
		char ans='Y';
		boolean valid=true;
		
		System.out.println("Byte type "+a);
		System.out.println("short type "+b);
		System.out.println("integer type "+d);
		System.out.println("long type "+d);
		System.out.println("float type "+x);
		System.out.println("double type "+y);
		System.out.println("character type"+ans);
		System.out.println("boolean type"+valid);
		System.out.println("String type "+name);
	}

}
